  public void load() {
    init();

    ResourceBundle fileNames = ResourceBundle.getBundle("de/schwarzdennis/hvm/bundles/fileNames");
    Map<String, String> fileNamesToKey = new HashMap<String, String>();
    for (String key : fileNames.keySet()) {
      fileNamesToKey.put(fileNames.getString(key), key);
    }

    File dir = new File(voicePackPath + File.separator + voicePackNameField.getText());
    if (dir.exists() && dir.isDirectory()) {
      for (String file : dir.list()) {
        if (file.indexOf('.') < 0) {
          continue;
        }
        String voiceName = file.substring(0, file.indexOf('.'));
        String keyForVoice = fileNamesToKey.get(voiceName);
        if (soundsByKey.get(keyForVoice) != null) {
          soundsByKey.get(keyForVoice).setText(dir.getAbsolutePath() + File.separator + file);
        }
      }
    }
  }

