  private void copyFile(String srcFileName, File voicePackPath, String voicePackName, String fileName) {
    String dstFileName = voicePackPath.getAbsolutePath() + File.separator + voicePackName + File.separator + fileName + ".ogg";

    if (dstFileName.equals(srcFileName)) {
      return;
    }

    FileChannel source = null;
    FileChannel destination = null;
    try {

      File src = new File(srcFileName);
      if (src.exists()) {

        File dst = new File(dstFileName);
        if (!dst.exists()) {
          dst.getParentFile().mkdirs();
          dst.createNewFile();
        }

        source = new FileInputStream(src).getChannel();
        destination = new FileOutputStream(dst).getChannel();
        destination.transferFrom(source, 0, source.size());
      }
    } catch (Exception e) {
      Logger.getLogger(Main.class.getName()).log(Level.WARNING, null, e);
    } finally {
      if (source != null) {
        try {
          source.close();
        } catch (IOException ex) {
        }
      }
      if (destination != null) {
        try {
          destination.close();
        } catch (IOException ex) {
        }
      }
    }
  }
